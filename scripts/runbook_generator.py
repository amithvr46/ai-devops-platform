# Feature 4 — AI Runbook Generator
# Converts raw incident notes into formatted runbooks
# Auto-commits the runbook to the repo

import os
import sys
from datetime import datetime

def generate_runbook(incident_notes: str) -> str:
    """Generate a formatted runbook from raw incident notes."""
    
    api_key = os.environ.get("ANTHROPIC_API_KEY")
    
    if api_key and api_key.startswith("sk-ant-"):
        try:
            import anthropic
            client = anthropic.Anthropic(api_key=api_key)
            prompt = (
                "You are a senior DevOps engineer writing a post-incident runbook.\n"
                "Convert these raw incident notes into a professional runbook.\n\n"
                "Format as:\n"
                "# Incident Runbook — [Title]\n"
                "## Summary\n"
                "## Impact\n"
                "## Timeline\n"
                "## Root Cause\n"
                "## Resolution Steps\n"
                "## Prevention\n"
                "## Lessons Learned\n\n"
                "Raw notes:\n" + incident_notes
            )
            message = client.messages.create(
                model="claude-sonnet-4-20250514",
                max_tokens=1500,
                messages=[{"role": "user", "content": prompt}]
            )
            return message.content[0].text
        except Exception as e:
            print(f"API call failed ({e}), using mock response\n")
            return get_mock_runbook(incident_notes)
    else:
        print("No API key found, using mock response\n")
        return get_mock_runbook(incident_notes)


