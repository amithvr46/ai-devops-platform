def save_runbook(runbook: str, title: str) -> str:
    """Save runbook to the runbooks folder."""
    
    # Create runbooks directory if it doesn't exist
    os.makedirs("runbooks", exist_ok=True)
    
    # Generate filename from date and title
    date = datetime.now().strftime("%Y-%m-%d")
    safe_title = title.lower().replace(" ", "-").replace("/", "-")[:30]
    filename = f"runbooks/{date}-{safe_title}.md"
    
    with open(filename, 'w') as f:
        f.write(runbook)
    
    return filename


